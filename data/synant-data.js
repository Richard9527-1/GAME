// ----- 工具函数 -----
function shuffle(arr) {
    for (var i = arr.length - 1; i > 0; i--) {
        var j = Math.floor(Math.random() * (i + 1));
        var temp = arr[i];
        arr[i] = arr[j];
        arr[j] = temp;
    }
    return arr;
}

// ----- 生成干扰项（从所有词库中选）-----
function generateOptions(correct, pool, allWords) {
    var opts = [correct];
    // 从所有词中选不同的干扰项
    var shuffled = shuffle(allWords.slice(0));
    for (var i = 0; i < shuffled.length; i++) {
        if (opts.length >= 4) break;
        if (shuffled[i] !== correct && opts.indexOf(shuffled[i]) === -1) {
            opts.push(shuffled[i]);
        }
    }
    while (opts.length < 4) {
        var fallback = ['开心', '高兴', '美丽', '漂亮', '快速', '勇敢', '善良', '聪明'];
        for (var i = 0; i < fallback.length; i++) {
            if (opts.length >= 4) break;
            if (opts.indexOf(fallback[i]) === -1) opts.push(fallback[i]);
        }
    }
    return shuffle(opts);
}

// ----- 生成近义词题 -----
function generateSynonymQuestions(difficulty) {
    var data = SYNONYM_DATA[difficulty] || SYNONYM_DATA.easy;
    var allWords = [];
    // 收集所有词
    for (var i = 0; i < data.synonyms.length; i++) {
        allWords.push(data.synonyms[i][0]);
        allWords.push(data.synonyms[i][1]);
    }
    for (var i = 0; i < data.antonyms.length; i++) {
        allWords.push(data.antonyms[i][0]);
        allWords.push(data.antonyms[i][1]);
    }
    allWords = shuffle(allWords);

    var result = [];

    // 近义词题
    for (var i = 0; i < data.synonyms.length; i++) {
        var pair = data.synonyms[i];
        var opts = generateOptions(pair[1], data.synonyms, allWords);
        result.push({
            question: '"' + pair[0] + '" 的近义词是？',
            answer: pair[1],
            options: opts,
            type: '近义词'
        });
        // 反向
        var opts2 = generateOptions(pair[0], data.synonyms, allWords);
        result.push({
            question: '"' + pair[1] + '" 的近义词是？',
            answer: pair[0],
            options: opts2,
            type: '近义词'
        });
    }

    // 反义词题
    for (var i = 0; i < data.antonyms.length; i++) {
        var pair = data.antonyms[i];
        var opts = generateOptions(pair[1], data.antonyms, allWords);
        result.push({
            question: '"' + pair[0] + '" 的反义词是？',
            answer: pair[1],
            options: opts,
            type: '反义词'
        });
        var opts2 = generateOptions(pair[0], data.antonyms, allWords);
        result.push({
            question: '"' + pair[1] + '" 的反义词是？',
            answer: pair[0],
            options: opts2,
            type: '反义词'
        });
    }

    return shuffle(result);
}

// ----- 确保每个难度 500+ 题（稳定版，不卡死）-----
function buildQuestionBank(difficulty) {
    var result = generateSynonymQuestions(difficulty);

    var target = 500;
    var attempts = 0;
    while (result.length < target && attempts < 100) {
        attempts++;
        var extra = generateSynonymQuestions(difficulty);
        for (var i = 0; i < extra.length; i++) {
            if (result.length >= target) break;
            var exists = false;
            for (var j = 0; j < result.length; j++) {
                if (result[j].question === extra[i].question) { exists = true; break; }
            }
            if (!exists) result.push(extra[i]);
        }
    }

    // 保底：如果还不够，用简单难度补充（防止卡死）
    if (result.length < target) {
        var easyExtra = generateSynonymQuestions('easy');
        for (var i = 0; i < easyExtra.length; i++) {
            if (result.length >= target) break;
            var exists = false;
            for (var j = 0; j < result.length; j++) {
                if (result[j].question === easyExtra[i].question) { exists = true; break; }
            }
            if (!exists) result.push(easyExtra[i]);
        }
    }

    return shuffle(result);
}

