// 获取一组词对（平民词和卧底词随机互换）
function getUndercoverPair() {
    var pair = UNDERCOVER_WORDS[Math.floor(Math.random() * UNDERCOVER_WORDS.length)];
    // 随机互换
    if (Math.random() > 0.5) {
        return { civilian: pair[0], undercover: pair[1] };
    } else {
        return { civilian: pair[1], undercover: pair[0] };
    }
}

// 获取多组词对（用于多轮游戏）
function getUndercoverPairs(count) {
    count = count || 5;
    var pairs = [];
    var used = new Set();
    var attempts = 0;
    while (pairs.length < count && attempts < 100) {
        attempts++;
        var pair = getUndercoverPair();
        var key = pair.civilian + '-' + pair.undercover;
        if (!used.has(key)) {
            used.add(key);
            pairs.push(pair);
        }
    }
    return pairs;
}

